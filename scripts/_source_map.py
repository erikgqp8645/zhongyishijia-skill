class SourceInfo(TypedDict):
    dynasty: str
    book: str
    author: str


# ── 辅助函数 ────────────────────────────────────────────────

def identify_source(card: dict) -> tuple[str, str, str]:
    """识别卡片对应的朝代、著作、作者"""
    source_ref = card.get("source_ref", "") or ""
    title = card.get("title", "") or ""
    summary = card.get("summary", "") or ""

    # 1. 先检查 source_ref 是否能直接匹配
    for key, info in SOURCE_MAP.items():
        if key in source_ref:
            return info

    # 2. 检查 title 中的关键词
    for key, info in SOURCE_MAP.items():
        if key in title:
            return info

    # 3. 检查 summary 中的关键词
    for key, info in SOURCE_MAP.items():
        if key in summary:
            return info

    # 4. 对于 zysjllsj/TypeID 格式, 从 TypeID 反查
    m = re.search(r"TypeID=(\d+)", source_ref)
    if m:
        tid = m.group(1)
        if tid in TYPEID_MAP:
            return TYPEID_MAP[tid]

    return ("待考", source_ref, "")


def identify_source_string(source_str: str, extra: str = "") -> tuple[str, str, str]:
    """从字符串（如 ChuChu 字段）识别朝代、著作、作者
    extra: 附加文本（如 ChuFang，处方中可能嵌有《书名》引用）"""
    # 合并搜索：source_str 优先，再搜 extra
    for text in (source_str, extra):
        for key, info in SOURCE_MAP.items():
            if key in text:
                return info
    return ("待考", source_str, "")


def sort_key(card: dict) -> tuple:
    """排序 key: 朝代顺序 → 著作 → 作者"""
    dyn, book, author = identify_source(card)
    return (DYNASTY_ORDER.get(dyn, 99), dyn, book, author)


def sort_key_str(dynasty: str, book: str = "", author: str = "") -> tuple:
    """字符串版的排序 key"""
    return (DYNASTY_ORDER.get(dynasty, 99), dynasty, book, author)
